int	ft_sqrt(int nb)
{
	int	num;

	if (nb == 1)
	{
		return (1);
	}
	else if (nb == 4)
	{
		return (2);
	}
	num = 0;
	while (num < nb / 2)
	{
		if (num * num == nb)
		{
			return (num);
		}
		num++;
	}
	return (0);
}
